public class Chair {
    private int legs;
    private String color;
public Chair()
{
legs=0;
color=" ";
}
public void setlegs(int l)
{
legs=l;
}
public int getlegs()
{
return legs;
}
public void setcolor(String c)
{
color=c;
}
public String getcolor()
{
return color;
}
    
}
